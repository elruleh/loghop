_language: Language = _DEFAULT_LANGUAGE


def _normalize_language(value: str | None) -> Language:
    normalized = (value or "").lower().replace("_", "-")
    if normalized.startswith("es"):
        return "es"
    return "en"


def _detect_language() -> Language:
    return _normalize_language(
        env.lang() or env.language() or locale.getlocale()[0] or env.locale_lang()
    )


def set_language(language: str) -> Language:
    global _language
    _language = _normalize_language(language)
    return _language


def get_language() -> Language:
    return _language


def tr(key: str, **values: object) -> str:
    catalog: Mapping[str, str] = _CATALOGS.get(_language, _CATALOGS[_DEFAULT_LANGUAGE])
    text = catalog.get(key) or _CATALOGS[_DEFAULT_LANGUAGE].get(key) or key
    return text.format(**values) if values else text


def init_from_environment() -> Language:
    return set_language(_detect_language())


init_from_environment()
